#include "cub3d.h"

static int	is_valid_char(char c)
{
	if (c == ' ' || c == '0' || c == '1')
		return (1);
	return (0);
}

int	map_validate(t_game *game)
{
	int	x;
	int	y;

	y = 0;
	while (y < game->map.height)
	{
		x = 0;
		while (x < game->map.width)
		{
			if (!is_valid_char(game->map.grid[y][x]))
				return (1);
			x++;
		}
		y++;
	}
	if (map_is_closed(game) != 0)
		return (1);
	return (0);
}
